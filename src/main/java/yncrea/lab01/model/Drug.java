package yncrea.lab01.model;

public class Drug {

    private String Name;
    private String Lab;

    public Drug() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getLab() {
        return Lab;
    }

    public void setLab(String lab) {
        Lab = lab;
    }
}
